package com.ju.simpleprograms.StudentScoreManage;

public class Student {

    private String name;
    private int Score;



    public void setName(String name) {
        this.name = name;
    }

    public void setScore(int score) {
        Score = score;
    }

    public String getName() {
        return name;
    }

    public int getScore() {
        return Score;
    }
}
